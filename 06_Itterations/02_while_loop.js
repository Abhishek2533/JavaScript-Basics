// WHILE loop
// WHILE runs until the condition is false

// Syntax 

// while (condition) {
//     console.log("condition is true");
// }

let i = 0;
while (i<=5) {
    console.log(i);
    i++;
}
// output -> 1 2 3 4 5

// DO WHILE loop

// Syntax
// do {
//     console.log("execute");
// } while (condition);

let val = 11;
do {
    console.log(`value is ${val}`);
} while (val <=5);

// in DOWHILE -> do code is execute before check the condition then it will check the condition i.e., it will execute 1 time definetely